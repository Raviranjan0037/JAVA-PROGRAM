package InputFromUserInJava;

import java.util.*;

public class TypeCastingFloatToInt {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		float number = 52.847f;
		int number2 = (int)number;
		System.out.println(number2);
	}

}




