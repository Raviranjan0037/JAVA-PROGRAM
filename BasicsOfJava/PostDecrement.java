package BasicsOfJava;

public class PostDecrement {
	public static void main(String[] args) {
	   int x = 8;
	   System.out.println(x);
	   x--;
	   System.out.println(x);
	   x--;
	   System.out.println(x);
	   x--;
	   System.out.println(x);
	   x--;
	   System.out.println(x);
	   x--;
	   System.out.println(x);
	}
}
