package BasicsOfJava;

public class ArimethicOperator {
	public static void main(String[] args) {
		int x = 6;
		int y = 8;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);
		System.out.println(y+x);
		System.out.println(y-x);
		System.out.println(y*x);
		System.out.println(y/x);
		System.out.println(y%x);
		System.out.println("THIS ALL IS ARITHMETIC OPERATOR");
	}

}
