package BasicsOfJava;

public class PostIncrement {
	public static void main(String[] args) {
		int x = 4;
		System.out.println(x);
		x++;
		System.out.println(x);
		x++;
		System.out.println(x);
		x++;
		System.out.println(x);
		x++;
		System.out.println(x);
		x++;
		System.out.println(x);
		x++;
		System.out.println(x);
	}

}
