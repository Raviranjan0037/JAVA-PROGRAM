package BasicsOfJava;

public class PreIncrement {
	public static void main(String[] args) {
		int x = 40;
		System.out.println(x);
		++x;
		System.out.println(x);
		++x;
		System.out.println(x);
		++x;
		System.out.println(x);
		++x;
		System.out.println(x);
		++x;
		System.out.println(x);
		++x;
		System.out.println(x);
	}

}