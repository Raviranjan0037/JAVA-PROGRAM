package BasicsOfJava;

public class DoubleFloutDataTypr {
	public static void main(String[] args) {
		double x = 5;
		double y = 4;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);
		
		
		
		
		
		double a = 8;
		double b = 6;
		double c = a/b;
		System.out.println(c);
		System.out.println(a);
		System.out.println(b);
		
	}
}
