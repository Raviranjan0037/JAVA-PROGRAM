package SwitchStatement;

public class CharacterSwitchStement {
		public static void main(String[] args) {
			char ch = 'a' ;
		
			
			switch(ch) {
			case 'a' : System.out.print("Litti-chokha");
			break;
			case 'b' : System.out.print("Chicken-Litti");
			break;
			case 'c' : System.out.print("Samosa");
			break;
			case 'd' : System.out.print("Momos");
			break;
			case 'e' : System.out.print("Coffee");
			break;
			case 'f' : System.out.print("None Of These");
			}
		}
}

